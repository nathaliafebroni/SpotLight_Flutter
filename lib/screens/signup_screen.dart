                      const SizedBox(
                        height: 25.0,
                      ),
                      // senha
                      TextFormField(
                        obscureText: true,
                        obscuringCharacter: '*',
                        validator: (value) {
                          if (value == null || value.isEmpty) {
                            return 'Por favor insira sua senha';
                          }
                          return null;
                        },
